def main():
    if not os.path.exists(JSON_PATH):
        print(f"❌ '{JSON_PATH}' 경로에서 원본 파일을 찾을 수 없습니다. 경로를 확인해 주세요.")
        return

    # 1. 원본 JSON 파일 읽기
    with open(JSON_PATH, "r", encoding="utf-8") as f:
        data = json.load(f)

    # 2. 강점 배열 순회하며 전수 패치 진행
    patched_count = 0
    missing_codes = []

    for s in data.get("strengths", []):
        code = s.get("code")
        
        if code in ALL_INTERPRETATIONS:
            # 기존에 이미 interpretation이 있어도 최신 전수 고도화 텍스트로 강제 대체 적용
            s["interpretation"] = {
                "complementary": ALL_INTERPRETATIONS[code]["complementary"],
                "undeveloped": ALL_INTERPRETATIONS[code]["undeveloped"]
            }
            patched_count += 1
        else:
            missing_codes.append(code)

    # 3. 데이터가 성공적으로 업데이트 되었으면 덮어쓰기
    if patched_count > 0:
        with open(JSON_PATH, "w", encoding="utf-8") as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
        
        print(f"==================================================")
        print(f"✅ [전수 고도화 완료] 'strengths.json' 패치 성공!")
        print(f"📦 패치 완료된 강점 노드 수: {patched_count}개 / 50개")
        print(f"📂 저장 경로: {JSON_PATH}")
        print(f"==================================================")
        
        if missing_codes:
            print(f"⚠️ 경고: 사전과 매핑되지 않은 일부 코드가 발견되었습니다: {missing_codes}")
    else:
        print("❌ 변경 사항이 적용되지 않았습니다.")

if __name__ == "__main__":
    main()